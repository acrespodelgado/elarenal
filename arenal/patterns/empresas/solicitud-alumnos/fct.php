                <h3 class="accent">¿En qué consiste la FCT?</h3>
                <p>La Formación en Centros de Trabajo (FCT) es un módulo profesional obligatorio que se cursa en todos los ciclos tanto de grado medio como de grado superior y que se desarrolla en la empresa.</p>
                <h4>Se caracteriza por:</h4>
                <ul class="my-3">
                    <li>Se programa previamente y es obligatoria para obtener el título.</li>
                    <li>Los alumnos realizan las actividades productivas propias del perfil profesional del título que se quiere conseguir.</li>
                    <li>Este módulo profesional se desarrolla en la empresa, donde se podrán observar y desempeñar las actividades y funciones propias de los distintos puestos de trabajo del perfil profesional y conocer la organización de los procesos productivos o de servicios y las relaciones laborales.</li>
                </ul>
                <p>Los alumnos estarán orientados y asesorados por dos personas clave en su proceso formativo:</p>
                <ul class="my-3">
                    <li>El profesor-tutor (del centro educativo).</li>
                    <li>El tutor laboral, monitor o instructor (de la empresa o entidad colaboradora).</li>
                </ul>
                <p>El módulo profesional de Formación en Centros de Trabajo tiene la misma estructura que el resto de módulos que componen los ciclos formativos de Formación Profesional, y al final tendrá una calificación de apto o no apto.</p>
                <h4>¿Qué se consigue?</h4>
                <ul class="my-3">
                    <li>Completar la adquisición de las competencias profesionales previstas en el ciclo formativo que se este cursando.</li>
                    <li>Adquirir conocimientos de la organización productiva. Contribuir al logro de las finalidades generales de la formación profesional, relacionadas con la integración en entornos de trabajo.</li>
                    <li>Evaluar la competencia profesional del alumno, en especial aquellos aspectos que no pueden comprobarse en el centro educativo por exigir situaciones reales de trabajo.</li>
                </ul>
                <h4>Duración</h4>
                <p>La duración de este módulo profesional está entre 260 y 410 horas según se trate de Formación Profesional Básica, de un ciclo de Grado Medio o de uno de Grado Superior
                En el caso de los cursos de especialización, la duración de este módulo profesional es menor.</p>
