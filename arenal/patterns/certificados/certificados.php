                <h1>¿Qué son los certificados profesionales, <span class="accent">los nuevos Grados C?</span></h1>
                <p class="big">En el CPIFP El Arenal, impartimos Grados C, Grados D y Grados E.</p>
                <p>Los certificados profesionales (Grados C), son títulos oficiales expedidos por el Ministerio de Educación, Formación Profesional y Deportes o en su caso, por las CCAA, que acreditan las competencias profesionales adquiridas. Las competencias profesionales comprenden los conocimientos y aptitudes necesarios para ejercer una actividad laboral concreta, en función de las necesidades del mercado de trabajo en cada momento.</p>
                <p>Dichas competencias hacen referencia a las cualificaciones del <strong>Catálogo Nacional de Cualificaciones Profesionales</strong> y puede ser acreditada parcialmente.</p>
                <p>Incluyen varios módulos profesionales y estancias formativas en empresas, con las que se obtiene experiencia en el sector y, en muchos casos, ayudan a la inserción laboral.</p>
                <p><i class="fa fa-clock-o accent"></i>La duración es variable, pudiendo ir desde las 350 a las 700 horas.</p>
